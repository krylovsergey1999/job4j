package ru.job4j.max;

public class Max {
    /**
     * Максимум из двух элементов использовал тернарный оператор.
     * @param first - число
     * @param second - число
     * @return - максимум из 2 чисел
     */
    public int max(int first, int second) {
        return first>second ? first : second;
    }

}
